/* ─────────────────────────────────────────────
   PAGE  (matches FeatureMain structure)
───────────────────────────────────────────── */
export default function PrivacyPolicy() {
  const [activeId, setActiveId] = useState("overview");
  const observerRef = useRef(null);

  useEffect(() => {
    observerRef.current = new IntersectionObserver(
      (entries) => {
        const visible = entries.find((e) => e.isIntersecting);
        if (visible) setActiveId(visible.target.id);
      },
      { rootMargin: "-40% 0px -50% 0px", threshold: 0.1 },
    );

    SECTIONS.forEach(({ id }) => {
      const el = document.getElementById(id);
      if (el) observerRef.current.observe(el);
    });

    return () => observerRef.current?.disconnect();
  }, []);

  return (
    <section className="relative w-full bg-linear-to-b from-gray-950 via-gray-900 to-gray-950">
      <PrivacyHero />

      {/* thin emerald divider, same as the shimmer lines in FeatureMain */}
      <div className="w-full h-px bg-linear-to-r from-transparent via-emerald-500/30 to-transparent" />

      <div className="max-w-7xl mx-auto px-4 md:px-12 py-20 flex gap-12">
        <TOC activeId={activeId} />

        <main className="flex-1 min-w-0 space-y-20 pr-4">
          {SECTIONS.map(({ id, number, title, subtitle, content }) => (
            <article key={id} id={id} className="scroll-mt-24">
              {/* header — mirrors the feature card header pattern */}
              <span className="text-emerald-400 text-sm font-medium">
                {subtitle}
              </span>
              <h2 className="mt-3 text-3xl md:text-4xl font-semibold text-gray-100 flex items-center gap-4">
                {title}
                <span className="text-sm font-mono text-gray-600">
                  {number}
                </span>
              </h2>

              {/* divider */}
              <div className="mt-5 mb-6 h-px w-full bg-linear-to-r from-emerald-500/20 via-gray-700 to-transparent" />

              {/* blocks */}
              <div>{content.map((block, i) => renderBlock(block, i))}</div>
            </article>
          ))}
        </main>
      </div>
    </section>
  );
}
