/* ─────────────────────────────────────────────
   BLOCK RENDERERS
───────────────────────────────────────────── */
function renderBlock(block, i) {
  switch (block.type) {
    case "text":
      return (
        <p key={i} className="mt-4 text-lg text-gray-300 leading-relaxed">
          {block.value}
        </p>
      );

    case "highlight":
      return (
        <div
          key={i}
          className="mt-6 border-l-2 border-emerald-500 bg-emerald-500/5 px-5 py-4 rounded-r-xl"
        >
          <p className="text-gray-300 leading-relaxed">{block.value}</p>
        </div>
      );

    case "subheading":
      return (
        <h3
          key={i}
          className="mt-8 mb-2 text-sm font-semibold uppercase tracking-widest text-emerald-400"
        >
          {block.value}
        </h3>
      );

    case "list":
      return (
        <ul key={i} className="mt-4 space-y-3">
          {block.items.map((item, j) => (
            <li
              key={j}
              className="flex items-start gap-3 text-gray-300 leading-relaxed"
            >
              <span className="mt-1.5 shrink-0 w-2 h-2 rounded-full bg-emerald-500/60" />
              {item}
            </li>
          ))}
        </ul>
      );

    case "grid":
      return (
        <div key={i} className="mt-6 grid grid-cols-1 sm:grid-cols-2 gap-4">
          {block.cards.map((card) => (
            <div
              key={card.label}
              className="bg-gray-800/50 border border-gray-700/60 rounded-2xl p-5 hover:border-emerald-500/30 transition-colors duration-300"
            >
              <p className="text-xs font-semibold uppercase tracking-widest text-emerald-400 mb-2">
                {card.label}
              </p>
              <p className="text-gray-300 text-sm leading-relaxed">
                {card.text}
              </p>
            </div>
          ))}
        </div>
      );

    case "contact":
      return (
        <div
          key={i}
          className="mt-6 bg-gray-800/50 border border-gray-700/60 rounded-2xl p-6 space-y-4"
        >
          {block.details.map(({ label, value, isEmail }) => (
            <div key={label} className="flex items-center gap-4">
              <span className="text-xs font-semibold uppercase tracking-widest text-gray-500 w-24 shrink-0">
                {label}
              </span>
              {isEmail ? (
                <a
                  href={`mailto:${value}`}
                  className="text-emerald-400 hover:text-emerald-300 transition-colors text-sm"
                >
                  {value}
                </a>
              ) : (
                <span className="text-gray-300 text-sm">{value}</span>
              )}
            </div>
          ))}
        </div>
      );

    default:
      return null;
  }
}

/* ─────────────────────────────────────────────
   TOC SIDEBAR
───────────────────────────────────────────── */
function TOC({ activeId }) {
  const scrollTo = (id) =>
    document
      .getElementById(id)
      ?.scrollIntoView({ behavior: "smooth", block: "start" });

  return (
    <aside className="hidden xl:block w-56 shrink-0 sticky top-28 self-start">
      <div className="sticky top-10">
        <p className="text-xs font-semibold uppercase tracking-widest text-emerald-400 mb-5">
          Contents
        </p>
        <nav className="space-y-1">
          {SECTIONS.map(({ id, number, title }) => (
            <button
              key={id}
              onClick={() => scrollTo(id)}
              className={`group w-full flex items-center gap-3 text-left px-3 py-2 rounded-lg text-xs border transition-all duration-200 ease-in-out ${
                activeId === id
                  ? "bg-emerald-500/10 text-emerald-400 border-emerald-400"
                  : "text-gray-500 border-transparent hover:text-gray-300 hover:bg-gray-800/50"
              }`}
            >
              <span
                className={`font-mono text-[10px] shrink-0 transition-colors ${
                  activeId === id
                    ? "text-emerald-500"
                    : "text-gray-600 group-hover:text-gray-400"
                }`}
              >
                {number}
              </span>
              {title}
            </button>
          ))}
        </nav>
      </div>
    </aside>
  );
}

